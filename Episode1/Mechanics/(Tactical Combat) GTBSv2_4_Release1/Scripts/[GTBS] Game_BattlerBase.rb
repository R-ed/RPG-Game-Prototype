class Game_BattlerBase
  #--------------------------------------------------------------------------
  # * Initialize
  #--------------------------------------------------------------------------
  alias gm_battlerbase_gtbs_init initialize unless $@
  def initialize
    gm_battlerbase_gtbs_init
    init_team;
  end
  #--------------------------------------------------------------------------
  # * Return Paralyzed
  #--------------------------------------------------------------------------
  def paralyzed?
    return state?(GTBS::PARALYZED_ID)
  end
  #--------------------------------------------------------------------------
  # * Return Sleeping? 
  #--------------------------------------------------------------------------
  def sleeping?
    return state?(GTBS::SLEEPING_ID)
  end
  #--------------------------------------------------------------------------
  # * Return Knocked Out
  #--------------------------------------------------------------------------
  def knocked_out?
    return state?(death_state_id)
  end
  #--------------------------------------------------------------------------
  # * Return Muted
  #--------------------------------------------------------------------------
  def muted?
    return self.restriction == 1
  end
  #--------------------------------------------------------------------------
  # * Determine if Character is in Berserker State
  #--------------------------------------------------------------------------
  def berserker?
    return (not @hidden and restriction == 1)
  end
end
